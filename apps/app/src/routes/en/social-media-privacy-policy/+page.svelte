	<h4>Data processing through social networks</h4>
	<p>
		We maintain publicly available profiles in social networks. The individual social networks we
		use can be found below.
	</p>
	<p>
		Social networks such as Facebook, X etc. can generally analyze your user behavior
		comprehensively if you visit their website or a website with integrated social media content
		(e.g., like buttons or banner ads). When you visit our social media pages, numerous data
		protection-relevant processing operations are triggered. In detail:
	</p>
	<p>
		If you are logged in to your social media account and visit our social media page, the operator
		of the social media portal can assign this visit to your user account. Under certain
		circumstances, your personal data may also be recorded if you are not logged in or do not have
		an account with the respective social media portal. In this case, this data is collected, for
		example, via cookies stored on your device or by recording your IP address.
	</p>
	<p>
		Using the data collected in this way, the operators of the social media portals can create user
		profiles in which their preferences and interests are stored. This way you can see
		interest-based advertising inside and outside of your social media presence. If you have an
		account with the social network, interest-based advertising can be displayed on any device you
		are logged in to or have logged in to.
	</p>
	<p>
		Please also note that we cannot retrace all processing operations on the social media portals.
		Depending on the provider, additional processing operations may therefore be carried out by the
		operators of the social media portals. Details can be found in the terms of use and privacy
		policy of the respective social media portals.
	</p>
	<h4>Legal basis</h4>
	<p>
		Our social media appearances should ensure the widest possible presence on the Internet. This is
		a legitimate interest within the meaning of Art. 6 (1) lit. f GDPR. The analysis processes
		initiated by the social networks may be based on divergent legal bases to be specified by the
		operators of the social networks (e.g., consent within the meaning of Art. 6 (1) (a) GDPR).
	</p>
	<h4>Responsibility and assertion of rights</h4>
	<p>
		If you visit one of our social media sites (e.g., Facebook), we, together with the operator of
		the social media platform, are responsible for the data processing operations triggered during
		this visit. You can in principle protect your rights (information, correction, deletion,
		limitation of processing, data portability and complaint) vis-&agrave;-vis us as well as
		vis-&agrave;-vis the operator of the respective social media portal (e.g., Facebook).
	</p>
	<p>
		Please note that despite the shared responsibility with the social media portal operators, we do
		not have full influence on the data processing operations of the social media portals. Our
		options are determined by the company policy of the respective provider.
	</p>
	<h4>Storage time</h4>
	<p>
		The data collected directly from us via the social media presence will be deleted from our
		systems as soon as you ask us to delete it, you revoke your consent to the storage or the
		purpose for the data storage lapses. Stored cookies remain on your device until you delete them.
		Mandatory statutory provisions - in particular, retention periods - remain unaffected.
	</p>
	<p>
		We have no control over the storage duration of your data that are stored by the social network
		operators for their own purposes. For details, please contact the social network operators
		directly (e.g., in their privacy policy, see below).
	</p>
	<h4>Your rights</h4>
	<p>
		You have the right to receive information about the origin, recipient and purpose of your stored
		personal data at any time and free of charge. You also have the right to object, the right to
		data portability and the right to file a complaint with the responsible regulatory agency.
		Furthermore, you can request the correction, blocking, deletion and, under certain
		circumstances, the restriction of the processing of your personal data.
	</p>
